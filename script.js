function findScholarships() {
    const qualification = document.getElementById('qualification').value;
    const type = document.getElementById('type').value;
    const scholarshipList = document.getElementById('scholarshipList');
    scholarshipList.innerHTML = ''; 

    if (!qualification || !type) {
        scholarshipList.innerHTML = "<p style='color: red;'>Please select both qualification and scholarship type.</p>";
        return;
    }

    const selectedScholarships = scholarships[type][qualification] || [];
    if (selectedScholarships.length > 0) {
        selectedScholarships.forEach(scholarship => {
            const item = document.createElement('div');
            item.className = 'scholarship-item';
            item.innerHTML = `
                <h3>${scholarship.name}</h3>
                <p>${scholarship.description}</p>
                <button onclick="openDetails('${scholarship.name}', '${scholarship.description}', '${scholarship.criteria}', '${scholarship.Registration}','${scholarship.link}')">View Details</button>
            `;
            scholarshipList.appendChild(item);
        });

        // Add "More Options" button
        const moreOptionsButton = document.createElement('button');
        moreOptionsButton.innerText = "More scholarships";
        moreOptionsButton.onclick = () => showAllScholarships(type);
        scholarshipList.appendChild(moreOptionsButton);
    } else {
        scholarshipList.innerHTML = "<p style='color: red;'>No scholarships available for this selection.</p>";
    }
}

function showAllScholarships(type) {
    const scholarshipList = document.getElementById('scholarshipList');
    scholarshipList.innerHTML = ''; 

    const allScholarships = [];
    for (const qualification in scholarships[type]) {
        allScholarships.push(...scholarships[type][qualification]);
    }

    if (allScholarships.length > 0) {
        allScholarships.forEach(scholarship => {
            const item = document.createElement('div');
            item.className = 'scholarship-item';
            item.innerHTML = `
                <h3>${scholarship.name}</h3>
                <p>${scholarship.description}</p>
                <button onclick="openDetails('${scholarship.name}', '${scholarship.description}', '${scholarship.criteria}', '${scholarship.Registration}','${scholarship.link}')">View Details</button>
            `;
            scholarshipList.appendChild(item);
        });
    } else {
        scholarshipList.innerHTML = "<p style='color: red;'>No scholarships available.</p>";
    }
}

